const RESULTS_KEY = 'wikigame-results'

export function getSavedResult(date: string): DailyResult | null {
  try {
    const raw = localStorage.getItem(RESULTS_KEY)
    const all: Record<string, DailyResult> = raw ? JSON.parse(raw) : {}
    return all[date] ?? null
  } catch {
    return null
  }
}

export function saveDailyResult(result: DailyResult): void {
  try {
    const raw = localStorage.getItem(RESULTS_KEY)
    const all: Record<string, DailyResult> = raw ? JSON.parse(raw) : {}
    all[result.date] = result
    localStorage.setItem(RESULTS_KEY, JSON.stringify(all))
  } catch {
    // localStorage full or unavailable
  }
}

export interface ShareInput {
  start: string
  end: string
  hops: number
  timeSeconds: number
  path?: string[]
  gaveUp?: boolean
  /** Daily games include this; random/custom games omit it. */
  challengeNumber?: number
}

export function buildShareText(input: ShareInput): string {
  const { start, end, hops, timeSeconds, path, gaveUp, challengeNumber } = input
  const time = timeSeconds < 60
    ? `${timeSeconds}s`
    : `${Math.floor(timeSeconds / 60)}m${Math.floor(timeSeconds % 60)}s`
  const header = challengeNumber != null
    ? `WikiGame #${challengeNumber}`
    : 'WikiGame (Random)'
  // For daily challenges, hide start→end in share text -- it spoils the puzzle.
  // Random/custom games still show it since there's no puzzle to spoil.
  const lines: string[] = [header]
  if (challengeNumber == null) {
    lines.push(`${start} \u2192 ${end}`)
  }
  lines.push(
    gaveUp
      ? `Gave up after ${hops} hop${hops === 1 ? '' : 's'} in ${time}`
      : `${hops} hops in ${time}`,
  )
  // Don't include bot info in share -- it's a hint
  lines.push('')
  if (path && path.length > 0) {
    // Fully censored path -- just emojis, no article names (no spoilers)
    const visited = new Set<string>()
    const emojis: string[] = []
    for (let i = 0; i < path.length; i++) {
      const isLast = i === path.length - 1
      const isFirst = i === 0
      const isBacktrack = !isFirst && visited.has(path[i])
      visited.add(path[i])
      // If the player gave up, the final node isn't a victory -- use a white
      // square to show they stopped without reaching the target.
      const lastEmoji = gaveUp ? '\u{2b1c}' : '\u{1f7e2}'
      emojis.push(isLast ? lastEmoji : isBacktrack ? '\u{1f7e5}' : '\u{1f7e9}')
    }
    lines.push(emojis.join(''))
  } else {
    lines.push(Array(Math.min(hops, 20)).fill('\u{1f7e9}').join(''))
  }
  lines.push('')
  lines.push(challengeNumber != null
    ? 'Play today\u2019s challenge:'
    : 'Try your own:')
  lines.push('jhomer192.github.io/wikigame')
  return lines.join('\n')
}
